CREATE TABLE `docs_client_action` (
  `id` bigint NOT NULL AUTO_INCREMENT COMMENT '自增主键',
  `uuid` varchar(128) NOT NULL COMMENT '文档唯一 ID',
  `action_name` varchar(128) NOT NULL COMMENT '客户端 Action 名称',
  `display_name` varchar(120) NOT NULL COMMENT '展示名称',
  `action_type` varchar(32) NOT NULL DEFAULT 'action' COMMENT 'Action 或 Controller',
  `category` varchar(64) NOT NULL DEFAULT 'custom' COMMENT 'Action 分类',
  `source_kind` varchar(64) NOT NULL COMMENT '来源类型',
  `source_package` varchar(128) NOT NULL COMMENT '来源包名',
  `source_file` text NOT NULL COMMENT '源码文件',
  `executor_name` varchar(128) NOT NULL COMMENT '执行器或选择器导出名',
  `description` text NOT NULL COMMENT 'Action 描述',
  `status` varchar(32) NOT NULL DEFAULT 'active' COMMENT '状态',
  `input_schema` json NOT NULL COMMENT '输入字段文档',
  `output_schema` json NOT NULL COMMENT '输出字段文档',
  `error_schema` json NOT NULL COMMENT '错误文档',
  `config_schema` json NOT NULL COMMENT '配置文档',
  `node_schema` json NOT NULL COMMENT 'Controller 节点规则',
  `runtime_schema` json NOT NULL COMMENT '运行时行为',
  `examples` json NOT NULL COMMENT '示例 DSL',
  `source_refs` json NOT NULL COMMENT '源码引用',
  `raw_meta` json NOT NULL COMMENT '抽取元信息',
  `created_at` timestamp(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6),
  `updated_at` timestamp(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6) ON UPDATE CURRENT_TIMESTAMP(6),
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_docs_client_action_uuid` (`uuid`),
  UNIQUE KEY `uk_docs_client_action_name` (`action_name`),
  KEY `idx_docs_client_action_category` (`category`),
  KEY `idx_docs_client_action_source_kind` (`source_kind`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci COMMENT='客户端 Action 文档表';

